public class Constant2 {

    // Static method to demonstrate constant time complexity
    static void ConstantTime2() {
        System.out.println("Hello, this is O(R) = O(1) = Constant");
        int a = 89;
        int b = 90;
        System.out.println("Sum of a and b: " + (a + b));
    }

    // Main method
    public static void main(String[] args) {
        Constant2.ConstantTime2(); // Call the ConstantTime2 method
    }
}
